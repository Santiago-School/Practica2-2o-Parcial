def mayordetres(num1, num2, num3):
    # comparamos los números y encontramos el mayor
    return max(num1, num2, num3)

# ejemplo de uso
numero1 = 6
numero2 = 26
numero3 = 16

mayor = mayordetres(numero1, numero2, numero3)

print("el mayor de los tres numeros es:", mayor)
